public class Livro {
	
    // Propriedades_Da_Classe_Livro //

	private	String Título;
	private	Double ISBN;
	private	String Autor;
	private String Editores;
	private Double anoDePublicação;
	
	public String getTítulo() {
		return Título;
	}
	public void setTítulo(String título) {
		Título = título;
	}
	public Double getISBN() {
		return ISBN;
	}
	public void setISBN(Double iSBN) {
		ISBN = iSBN;
	}
	public String getAutor() {
		return Autor;
	}
	public void setAutor(String autor) {
		Autor = autor;
	}
	public String getEditores() {
		return Editores;
	}
	public void setEditores(String editores) {
		Editores = editores;
	}
	public Double getAnoDePublicação() {
		return anoDePublicação;
	}
	public void setAnoDePublicação(Double anoDePublicação) {
		this.anoDePublicação = anoDePublicação; }
}
	

	
	